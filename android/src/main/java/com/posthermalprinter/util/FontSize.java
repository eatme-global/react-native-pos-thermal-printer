package com.posthermalprinter.util;

/**
 * Represents the various font sizes available for printing.
 */
public enum FontSize {
  /**
   * Normal font size.
   */
  NORMAL,

  /**
   * Tall font size, typically used for vertically enlarged text.
   */
  TALL,

  /**
   * Wide font size, typically used for horizontally enlarged text.
   */
  WIDE,

  /**
   * Big font size, typically used for both vertically and horizontally enlarged text.
   */
  BIG
}
