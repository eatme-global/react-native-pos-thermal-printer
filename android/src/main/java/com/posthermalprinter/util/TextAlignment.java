package com.posthermalprinter.util;

/**
 * Represents the alignment options for text in a print job.
 */
public enum TextAlignment {
  /**
   * Aligns the text to the left side of the paper.
   */
  LEFT,

  /**
   * Centers the text on the paper.
   */
  CENTER,

  /**
   * Aligns the text to the right side of the paper.
   */
  RIGHT
}
